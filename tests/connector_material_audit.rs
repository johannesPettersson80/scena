#[test]
fn connector_assets_material_audit_identifies_bright_mapless_placeholder_materials() {
    let waterbottle = audit_materials("tests/assets/gltf/khronos/WaterBottle/WaterBottle.gltf");
    let connector_reports = CONNECTOR_ASSETS
        .iter()
        .map(|path| audit_materials(path))
        .collect::<Vec<_>>();

    let mut report = String::new();
    writeln!(
        &mut report,
        "# Connector Material Audit\n\nThis report is generated by `{}`.\n",
        module_path!()
    )
    .expect("write report header");
    writeln!(
        &mut report,
        "## Summary\n\n- Connector assets audited: {}\n- WaterBottle reference audited: yes\n",
        connector_reports.len()
    )
    .expect("write summary");
    for asset in &connector_reports {
        write_asset_report(&mut report, asset);
    }
    writeln!(&mut report, "## Reference\n").expect("write reference header");
    write_asset_report(&mut report, &waterbottle);

    std::fs::create_dir_all(
        Path::new(REPORT_PATH)
            .parent()
            .expect("report path has a parent"),
    )
    .expect("create material audit artifact dir");
    std::fs::write(REPORT_PATH, report).expect("write connector material audit report");

    let flat_connector_materials: usize = connector_reports
        .iter()
        .map(|asset| {
            asset
                .materials
                .iter()
                .filter(|material| material.is_mapless_placeholder())
                .count()
        })
        .sum();
    assert_eq!(
        flat_connector_materials, 0,
        "connector assets must not contain remaining mapless flat materials"
    );
    assert!(
        waterbottle
            .materials
            .iter()
            .any(MaterialAudit::has_full_pbr_texture_set),
        "WaterBottle reference must keep baseColor, metallicRoughness, normal, and occlusion maps"
    );
}

